/*
	Primitive Data Type :- Tha data type which we cannot break into any other data
	                       type is called primitive data type.
*/

class Primitive {

	public static void main(String[] args){

		int rollNo = 45;
		char ch = 'A';
		float marks = 98.67f;
		double largeDecimalNumber = 67868.8789;
		long largeIntValue = 789737389899L;
		boolean check = true;

		// String is not a primitive data type
		String name = "Rohit";
	}
}

			

